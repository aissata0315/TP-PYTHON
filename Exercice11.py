a = int (input("donnez la valeur de a"))
b = int(input("donnez la valeur de b"))
o = str(input("quel operation souhaiterai vous faire?"))

if o == '+' :
    resultat = a+b
    print(resultat)
if  o == "-" :
    resultat = a-b
    print(resultat)
if  o == "*" :
    resultat = a*b
    print(resultat)
if  o == "/" :
    resultat = a/b
    print(resultat)


